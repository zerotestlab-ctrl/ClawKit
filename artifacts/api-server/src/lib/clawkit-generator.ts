export function runSafetyAudit(product: Product): { score: number; issues: SafetyIssue[] } {
  const issues: SafetyIssue[] = [];

  if (!product.apiSpecContent && !product.apiSpecUrl) {
    issues.push({
      severity: "info",
      category: "API Specification",
      description: "No API specification provided",
      fix: "Upload an OpenAPI spec to enable full security scanning and better agent compatibility.",
    });
  }

  if (!product.websiteUrl) {
    issues.push({
      severity: "info",
      category: "Endpoint Verification",
      description: "No website URL provided for endpoint verification",
      fix: "Add your API's base URL to enable endpoint reachability checks.",
    });
  }

  const injectionPatterns = ["eval(", "exec(", "system(", "shell_exec", "__import__"];
  const descLower = product.description.toLowerCase();
  if (injectionPatterns.some((p) => descLower.includes(p))) {
    issues.push({
      severity: "critical",
      category: "Prompt Injection",
      description: "Potential code execution patterns detected in description",
      fix: "Remove any code execution patterns from the product description. Use safe, descriptive language only.",
    });
  }

  if (product.description.length < 50) {
    issues.push({
      severity: "warning",
      category: "Description Quality",
      description: "Product description is too short for reliable agent understanding",
      fix: "Expand the description to at least 100 characters with clear capability and usage information.",
    });
  }

  if (product.apiSpecContent && product.apiSpecContent.includes("password")) {
    issues.push({
      severity: "warning",
      category: "Sensitive Data Exposure",
      description: "API spec may expose password fields in plain text",
      fix: "Ensure password fields use write-only mode and are never returned in API responses.",
    });
  }

  const criticalCount = issues.filter((i) => i.severity === "critical").length;
  const warningCount = issues.filter((i) => i.severity === "warning").length;

  let score = 100;
  score -= criticalCount * 25;
  score -= warningCount * 10;
  score = Math.max(0, Math.min(100, score));

  return { score, issues };
}

