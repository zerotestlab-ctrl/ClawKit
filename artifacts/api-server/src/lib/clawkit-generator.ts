export function generateMcpManifest(product: Product): string {
  const manifest = {
    schema_version: "2026-01",
    name: product.name.toLowerCase().replace(/\s+/g, "-"),
    display_name: product.name,
    description: product.description,
    version: "1.0.0",
    author: "ClawKit Distribution",
    distribution: {
      platform: "clawkit",
      published_at: new Date().toISOString(),
      mcp_compliant: true,
    },
    capabilities: {
      tools: true,
      resources: true,
      prompts: true,
      sampling: false,
    },
    endpoints: product.websiteUrl
      ? [{ name: "api", url: product.websiteUrl, description: "Primary API endpoint" }]
      : [],
    api_spec: product.apiSpecUrl
      ? { url: product.apiSpecUrl, format: "openapi" }
      : product.apiSpecContent
        ? { inline: true, format: "openapi" }
        : null,
    runtime_compatibility: {
      chatgpt_actions: true,
      claude_tools: true,
      grok_functions: true,
      moltbook: true,
      openclaw: true,
      cli: true,
      direct_api: true,
    },
    safety: {
      injection_protected: true,
      rate_limited: true,
      audit_trail: true,
      clawkit_verified: true,
    },
    distribution_channels: ["MCP", "ChatGPT Apps", "Claude Marketplace", "Moltbook", "OpenClaw", "CLI"],
  };
  return JSON.stringify(manifest, null, 2);
}

